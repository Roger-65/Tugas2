public class ASIGNi {
	public static void main(String [] args) {
		short ks = 1;
		int ki = 1;
		long kl = 10000;
		char C = 65;
		char Cl = 'Z';
		double x = 50.2f;
		float y = 50.2f;
		System.out.println("Karakter  = " + C);
		System.out.println("Karakter  = " + Cl);
		System.out.println("Bilangan integer (short) = " + ks);
		System.out.println("\t\t (int)   = " + ki);
		System.out.println("\t\t (long)  = " + kl);
		System.out.println("Bilangan Real x = " + x);
		System.out.println("Bilangan Real y = " + y);
	}
}
