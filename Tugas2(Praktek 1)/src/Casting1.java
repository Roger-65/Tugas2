public class Casting1 {
	public static void main(String [] args) {
		int a = 5, b = 6;
		float d =2.f, e = 3.2f;
		char g = '5';
		double k = 3.14;
		System.out.println("int ke float    : " + (float)a);
		System.out.println("int ke double   : " + (double)b);
		System.out.println("float ke int    : " + (int)d);
		System.out.println("float ke double : " + (double)e);
		System.out.println("char ke int     : " + (int)g);
		System.out.println("char ke float   : " + (float)g);
		System.out.println("char ke double  : " + (double)g);
		System.out.println("double ke int   : " + (int)k);
		System.out.println("double ke float : " + (float)k);
	}
}
