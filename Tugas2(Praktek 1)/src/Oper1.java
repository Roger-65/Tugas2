public class Oper1 {
	public static void main(String [] args) {
		int n = 10,x = 1, y =2;
		System.out.println("n = " + n);
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println("n & ~ 8 = " + (n & 8));
		System.out.println("y << 2  = " + (y << 2));
		System.out.println("y >> 3  = " + (y >> 3));
	}
}
