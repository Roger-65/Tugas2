public class KasusBoolean {
	public static void main(String [] args) {
		boolean bool;
		bool = true;
		if(bool) {
			System.out.print("true\n");
		}else {
			System.out.print("false");
		}
		System.out.println();
		if(!bool) {
			System.out.print("Salah\n");
		}else {
			System.out.print("Benar");
		}
	}
}
